double amp2(char* proc, char* remove, char* soName, double Pin, char* m1Name, double m1V, double hel1, char* m2Name, double m2V, double hel2, char* m3Name, double m3V, char* m4Name, double m4V, char* otherParamName1, double otherParamValue1, char* otherParamName2, double otherParamValue2)
{ int err;

  REAL pvec[16];
  REAL *p1=pvec, *p2=pvec+4, *p3=pvec+8, *p4=pvec+12;

  REAL m1,m2,m3,m4,m[4];

  double  Pout;
  double  totcoef, sqrt_S, S, lambda12, lambda34,ms,md;
  double  cos_fi, cos_step;
  double  sigmaTot=0;
  int i;
  char mess[20];
  double GG;
  numout*cc;
  txtList decays;

  printf("Calculating CH M^2 for %s and removing %s.\n",proc, remove);
  printf("Pin = %f\n",Pin);

// Specify model for work: Model directory and model number.
  setModel("models" ,1 ); 

  
/* Assign Parameters */
  GG=1.238;
  printf("EE=%E\n",findValW("EE"));
  printf("GG=%E\n",GG);
  printf("MW=%E\n",findValW("MW"));
  assignValW("wW",0);
  printf("WW=%E\n",findValW("wW"));
  printf("WZ=%E\n",findValW("wZ"));
  printf("SW=%E\n",findValW("SW"));
  printf("Mh=%E\n",findValW("Mh"));
  printf("wh=%E\n",findValW("wh"));
  if(strlen(otherParamName1)>0){
    assignValW(otherParamName1, otherParamValue1);
    printf("%s=%E\n",otherParamName1, findValW(otherParamName1));
  }
  if(strlen(otherParamName2)>0){
    assignValW(otherParamName2, otherParamValue2);
    printf("%s=%E\n",otherParamName2, findValW(otherParamName2));
  }

  if(strlen(m1Name)>0){
    assignValW(m1Name, m1V);
    printf("1: %s=%E\n",m1Name,findValW(m1Name));
  }
  else printf("1: 0\n");
  if(strlen(m2Name)>0){
    assignValW(m2Name, m2V);
    printf("2: %s=%E\n",m2Name,findValW(m2Name));
  }
  else printf("2: 0\n");
  if(strlen(m3Name)>0){
    assignValW(m3Name, m3V);
    printf("3: %s=%E\n",m3Name,findValW(m3Name));
  }
  else printf("3: 0\n");
  if(strlen(m4Name)>0){
    assignValW(m4Name, m4V);
    printf("4: %s=%E\n",m4Name,findValW(m4Name));
  }
  else printf("4: 0\n");

  // Calculation of public constraints  
  err=calcMainFunc();
  if(err) { printf("Can not calculate constrained parameter %s\n",varNames[err]);return err;}

  //slhaDecayPrint("W+", 0,stdout);

// SQME code generation 
  cc = getMEcode(0, 1, proc, remove, "", soName);
  err=passParameters(cc);
  if(err) { printf("Can not calculate constrained parameter %s\n",cc->interface->varName[err]); return err;}

  
  printf("MZ=%E\n",findValW("MZ"));

  
/* find masses */ 
  procInfo2(cc,1,NULL, m);
  m1=m[0];m2=m[1];m3=m[2];m4=m[3];

  if(strlen(m1Name)==0){
    //Choose Helicity
    Helicity[0] = hel1;
    printf("Hel1 = %Lf\n", Helicity[0]);
  }
  if(strlen(m2Name)==0){
    //Choose Helicity
    Helicity[1] = hel2;
    printf("Hel2 = %Lf\n", Helicity[1]);
  }

/* 2-2 kinematics */
  sqrt_S=sqrt(m1*m1+Pin*Pin) +sqrt(m2*m2+Pin*Pin);
  S=sqrt_S*sqrt_S;  
  lambda12=2*sqrt_S*Pin;
  ms = m3+m4; if (ms >= sqrt_S) return 1;
  md = m3 -m4;
  lambda34 = sqrt((S - ms*ms) * (S - md*md));   
  Pout=lambda34/(2*sqrt_S);

/* fill momenta of particles */
  
  for(i=0;i<16;i++) pvec[i]=0;
  p1[0]=  sqrt(Pin*Pin + m1*m1);
  p1[3]=  Pin;
  p2[0]=  sqrt(Pin*Pin + m2*m2);
  p2[3]= -Pin;
  p3[0]=  sqrt(Pout*Pout + m3*m3);
  p4[0]=  sqrt(Pout*Pout + m4*m4);
  
  
  cos_step=0.1; /* step to calculate dsigma/dcos */
/* cycle */
  printf("ldouble dataCH[20] = {");
  for(cos_fi=1-cos_step/2; cos_fi>-1; cos_fi -= cos_step)
  { double  DsigmaDcos;
    double  sin_fi=sqrt((1-cos_fi)*(1+cos_fi));
    int err=0;
    double amp2;
    p3[3]=Pout*cos_fi; p4[3]=-p3[3];
    p3[2]=Pout*sin_fi; p4[2]=-p3[2];
    //printf("GG=%E",GG);
    amp2 = cc->interface->sqme(1,GG,pvec,NULL,&err);
    //printf("GG=%E",GG);
    //printf("amp2 = %.10E @ cos(theta)=%f\n",amp2,cos_fi);
    printf("%.15E",amp2);
    if(cos_fi>-0.9) printf(",");
  }
  printf("};\n");
  
  return 0;
}
